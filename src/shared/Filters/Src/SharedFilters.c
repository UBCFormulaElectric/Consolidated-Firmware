void SharedFilter_LowPassFilter(uint32_t *input, uint32_t *output, uint32_t smoothing_factor)
{
    for (uint32_t i = 0; i < sizeof(output)/sizeof(output[0]); i++)
    {
        output[i] = output[i] + smoothing_factor * (input[i] - output[i]);
    }
}
